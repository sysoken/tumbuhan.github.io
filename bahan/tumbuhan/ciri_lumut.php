        <div class="penjelasan">
          <h1>Penjelasan Isi Vidio :</h1>
          <p>Tumbuhan lumut merupakan bentuk peralihan antara Thallophyta atau tumbuhan
              bertalus atau belum memiliki akar, batang dan daun sejati dengan cormophyta atau tumbuhan
              berkormus atau sudah memiliki akar, batang dan daun sejati.</p>
          <ul>
            <li>Tumbuhan lumut bentuk peralihan antara Thallophyta dan Cormophyta</li>
            <li>Berklorofil dapat berfotosintesis karena memiliki zat hujau daun</li>
            <li>Umumnya hidup di tempat yang lembab</li>
            <li>Sebagian besar hidup di daratan atau terestrial</li>
            <li>Tidak memiliki pembuluh angkut, tubuh lumut ada yang berbentuk lembaran,
                contohnya Hepaticopsida. Ada juga yang berbentuk seperti tumbuhan kecil dan tegak,
                contohnya Bryopsida.</li>
            <li>Mengalami pergiliran keturunan atau metagenesis dari fase gametofit ke fase sporofit.</li>
          </ul>
          <br>
          <p>Perbedaan fase gametofit dan fase sporofit :</p>
          <ul>
            <li>Fase gametofit, merupakan fase yang lebih dominan artinya memiliki masa hidup yang
                lama, pada fase gamotofit tumbuhan lumut tampak sebagai tumbuhanyang berwarna
                hijau. Menghasilkan gamet atau sel kelamin. Sel kelamin jantam atau spermatozoid
                dihasilkan oleh anteridium. Sel kelamin betina atau ovum dihasilkan oleh arkegonium.</li>
            <li>Fase sporofit, masa hidonya lebih pendek, menghasilkan spora, menumpang di atas
                gametofit, bertangkai, berbentuk seperti terompet atau kapsul. Mendapatkan air, garam
                mineral, dan zat makanan dari gametofit.</li>
          </ul>
