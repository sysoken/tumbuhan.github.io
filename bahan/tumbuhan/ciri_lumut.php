      <h3><p class="textjudul"> Ciri-ciri Tumbuhan Lumut</p></h3>
      <div class="canvas">
        <p>Lumut memiliki klorofil sehingga dapat berfotosintesis. Sebagian besar lumut
            merupakan tumbuhan terestril atau hidup di daratan lumut mudah ditemukan, terutama di
            tempat yang lembab (higrofit), di tanah, tembok, bebatuan lapuk, dan menempel (epifit) di kulit
            pohon. Namun, ada pula lumut yang hidup di air (hidrofit), misalnya Ricciocarpus natans. Di
            tempat lembab dan teduh, lumut tumbuh subur dan tampak sebgai hamparan hijau
        </p>
        <br>
        <p>
        Ciri-ciri Tumbuhan Lumut (Bryophyta)
        </p>
        <ol type='a'>
        <li>Talofita yaitu tumbuhan yang tidak bisa dibedakan antara akar, batang dan daun.</li>
        <li>Kormofita yaitu suatu tumbuhan yang sudah bisa dibedakan antara akar, batang dandaun</li>
        <li>Tumbuhan lumut disebut juga dengan tumbuhan peralihan karena ada berupa
        tumbuhan yang masih berupa talus (lembaran, yakni lumut hati), tetapi ada juga yang
        sudah mempunyai struktur tubuh mirip dengan akar, batang dan daun sejati (lumut
        daun).</li>
        <li>tumbuhan lumut juga merupakan suatu tumbuhan pelopor (vegetasi perintis), yang
        tumbuh disuatu tempat sebelum tumbuhan lain mampu tumbuh.</li>
        <li>Tumbuhan ini berukuran : makroskopis 1-2 cm, dan ada juga yang mencapai 40 cm.</li>
        <li>Tumbuhan ini tubuh nya berbentuk : mempunyai dua bentuk generasi, yakni generasi
        Gametofit dan generasi Sporofit</li>
        </ol>

