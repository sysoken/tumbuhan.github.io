      <h3> <img src="../gambar/microscope.png"><p class="textjudul">Ayo Mengamati</p></h3>
      <div class="canvas">
        <p>Pembelajaran kita kali ini diawali dengan membahas tentang lumut yang merupakan salah satu
          kelompok tumbuhan pada Kingdom Plantae. Lumut (Bryophita) berasal dari bahasa Yunani yaitu bryon
          yang berarti “Tumbuhan Lumut“. Pada umumnya, lumut berwarna hijau karena mempunyai sel– sel
          yang memiliki pigmen hijau berupa klorofil sehingga lumut memiliki kemampuan untuk menghasilkan
          senyawa organik melalui proses fotosintesis yang terjadi di dalamnya. Itulah sebabnya lumut tergolong
          organisme fotoautotro.
        </p>
