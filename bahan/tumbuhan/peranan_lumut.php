      <h3 style="width: 300px;"> <img src="../gambar/microscope.png" width="20px" height="40px"><p class="textjudul">Peranan Lumut Bagi Manusia</p></h3>
      <div class="canvas">
        <p>Tumbuhan lumut dalam beberapa jenis tumbuhan memiliki manfaat atau peranan tumbuhan
lumut bagi kehidupan manusia.</p>
<p>Manfaat Tumbuhan Lumut (Bryophyta) adalah sebagai berikut :</p>
        <ol type='a'>
        <li>Sebagai obat hepatitis (Marchantia polymorpha)</li>
        <li>Bahan pembalut dan bahan bakar (Spagnum)</li>
        <li>Sebagai penyedia sumber air pada saat musim kemarau</li>
        <li>Sebagai penyedia oksigen untuk lingkungannya, karena tumbuhan lumut berklorofil sehinga
dapat melakukan fotosintesis yang salah satu hasilnya adalah oksigen</li>
        <li>Sebagai obat antiseptik (Frullania tamarisci jenis lumut hati)</li>
        <li>Mengandung senyawa yang dapat mengobati penyakit jantung (Cratoneuron filicinun jenis
lumut daun)</li>
        <li>Membantu mengobati penyakit pneumonia (Haplocaldium catillatum jenis lumut daun)</li>
