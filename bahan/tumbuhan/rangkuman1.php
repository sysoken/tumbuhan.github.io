      <h3> <img src="../gambar/benar.png"><p class="textjudul">Rangkuman</p></h3>
      <div class="canvas">
        <ul>
          <li>Berdasarkan struktur tubuhnya, tumbuhan lumut masih berupa talus karena belum mempunyai
akar, batang dan daun sejati.</li>
          <li>Bagian tubuh pada lumut yang memiliki kemiripan dengan akar disebut Rizoid. Rizoid
memiliki fungsi untuk menyerap air dan garam mineral serta sebagai alat perlekatan pada
habitatnya.</li>
          <li>Daun lumut pada umumnya setebal satu lapis sel, kecuali ibu tulang daun, lebih dari satu lapis
sel. Sel-sel daunnya kecil, sempit, panjang, dan mengandung kloroplas yang tersusun seperti
jala. Dapat dibedakan antara bagian yang berfungsi sebagai penghasil spora (sporofit) dan
bagian penghasil gamet (gametofit)ny</li>
          <li>Lumut hidup di darat, tidak berkormus, dan memiliki pergiliran keturunan.</li>
          <li>Reproduksi lumut terdiri atas 2 fase yaitu fase aseksual dan seksual. Reproduksi aseksual dan
seksual berlangsung secara bergantian melalui suatu pergiliran keturunan yang disebut
Metagenesis. Reproduksi aseksualnya dengan menghasilkan spora haploid yang dibentuk
dalam sporofit sedangkan reproduksi seksualnya dengan menghasilkan gamet, baik jantan
maupun betina yang dibentuk dalam gametofit</li>
          <li>Tidak memiliki floem dan xylem yang berfungsi sebagai pembuluh angkut. Karena itulah,
lumut sangat menyukai tempat yang lembab</li>
          <li>Klasifikasi Bryophyta berdasar bentuk sporangiumnya., dibagi menjadi 3 kelas yaitu: lumut
hati, lumut daun, lumut tanduk.</li>
        </ul>
