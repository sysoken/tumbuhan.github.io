      <h3> <img src="../gambar/microscope.png"><p class="textjudul">Ayo Mengamati</p></h3>
      <div class="canvas">
        <p>Lumut terdiri dari 3 Devisio yaitu Bryophyta, Hepaticophyta, dan Anthocerotophyta.
        </p>
        <ol type='a'>
        <li>Lumut Daun (Bryophyta)</li>
        <p>Lumut daun merupakan lumut yang paling banyak dikenal. Bryophyta mempunyai
struktur seperti akar yang disebut rizoid, struktur seperti batang, dan struktur seperti daun.
Tubuh fase gametofit lumut daun memiliki gametangium di bagian atasnya. Kebanyakan
spesies lumut menghasilkan gamet berbeda sehingga dapat dibedakan antara tumbuhan jantan
dan tumbuhan betina. Akan tetapi, ada juga yang menghasilkan anteridium dan arkegonium
pada satu tumbuhan. Tubuh fase sporofit yang dihasilkan akan tumbuh di bagian atas tubuh
gametofit betina. Sporofit akan terus menempel pada gametofit dan bergantung untuk
memperoleh nutrisi. Setelah dewasa, sporofit akan berubah warna menjadi kecokelatan.</p>
<p>Pada beberapa spesies sporangium dilapisi struktur seperti tudung yang disebut kaliptra
yang dihasilkan oleh arkegonium. Jika spora lumut sampai ke lingkungan yang sesuai, spora
itu akan berkecambah dan tumbuh menjadi filamen yang disebut Protonema. Contoh lumut ini
antara lain Polytrichum juniperinum, Funaria, Pogonatum cirratum, Aerobryopsis longissima,
dan lumut gambut sphagnum</p>
        <br>
        <center>
          <img height="200" width="300" src="gambar/th.jpg"/>
          <br>
          <i>Gambar 3. Polytrichum Juniperinum.</i>
        </center>
        <br>
        <li>Lumut Hati (Hepaticophyta)</li>
        <p>Lumut hati mencakup 6.000 spesies tumbuhan tak berpembuluh. Bentuk tubuh
gametofit lumut hati berbeda dengan gametofit lumut daun. Pada lumut hati tubuhnya tersusun
atas struktur berbentuk hati pipih, disebut talus, yang tidak terdiferensiasi menjadi akar, batang,
dan daun. Tubuhnya terbagi menjadi dua lobus sehingga tampak seperti lobus pada hati.</p>
<p>Siklus hidup lumut hati mirip dengan lumut daun, walaupun bentuk tubuhnya agak
berbeda. Di dalam sporangium terdapat sel yang berbentuk gulungan yang disebut elatera.
Elatera akan terlepas saat kapsul terbuka, sehingga membantu memencarkan spora. Pada
beberapa lumut hati, gametangium berada pada struktur batang yamg disebut arkegoniofor
(yang menghasilkan arkegonium) dan anteridiofor (yang menghasilkan anteridium). Lumut hati
juga dapat melakukan reproduksi aseksual dengan sel yang disebut gemma yang merupakan struktur seperti mangkok di permukaan gametofit. Contoh lumut hati adalah Marchantia
polymorpha dan Porella.</p>
        <br>
        <center>
          <img height="200" width="300" src="gambar/hati.jpg"/>
          <br>
          <i>Gambar 4. Marchantia polymorpha</i>
        </center>
        <br>
        <li>Lumut Tanduk (Anthocerotophyta)</li>
        <p>Lumut tanduk mempunyai gametofit mirip dengan gametofit lumut hati, perbedaannya
hanya terletak pada sporofitnya. Sporofit lumut tanduk mempunyai kapsul memanjang yang
tumbuh seperti tanduk dari gametofit. Masing– masing mempunyai kloroplas tunggal yang
berukuran besar, lebih besar dari kebanyakan lumut. Contohnya adalah Anthoceros natans.Pada
spesies ini arkegonium dan anteridium melekat pada talus gametofit. Ciri unik dari lumut
tanduk adalah sporofit akan terus tumbuh selama masa hidup gametofit.</p>
<br>
        <center>
          <img height="200" width="300" src="gambar/lumut tanduk.jpg"/>
          <br>
          <i>Gambar 5. Lumut Tanduk</i>
        </center>
        <br>
        </ol>
